    	$model =  $this->findModel($article);
    	if($model)
    	{
			Yii::$app->opengraph->set([
			    'title' => $model->name,
			    'description' => $model->meta_description,
			]);
		}
    	  
        return $this->render('view', [
            'model' => $model,
